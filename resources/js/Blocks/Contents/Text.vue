<template>
    <div :class="wrapperClass">
        <biz-toolbar-content
            @delete-content="deleteContent"
            @duplicate-content="duplicateContent"
        />
        <div
            class="content"
            :class="contentClass"
        >
            <biz-tinymce
                v-model="entity.content.html"
                :class="editorClass"
            />
        </div>
    </div>
</template>

<script>
    import MixinDeletableContent from '@/Mixins/DeletableContent';
    import MixinDuplicableContent from '@/Mixins/DuplicableContent';
    import BizTinymce from '@/Biz/EditorTinymce';
    import BizToolbarContent from '@/Blocks/Contents/ToolbarContent';
    import { concat } from 'lodash';
    import { createMarginClasses, createPaddingClasses } from '@/Libs/page-builder';
    import { useModelWrapper } from '@/Libs/utils';

    export default {
        name: 'Text',
        mixins: [
            MixinDeletableContent,
            MixinDuplicableContent
        ],
        components: {
            BizTinymce,
            BizToolbarContent,
        },
        props: {
            id: String,
            modelValue: Object,
        },
        setup(props, { emit }) {
            return {
                config: props.modelValue.config,
                entity: useModelWrapper(props, emit),
            };
        },
        computed: {
            contentClass() {
                return concat(
                    (this.config.text?.alignment ?? ''),
                    (this.config.text?.size ?? '')
                ).filter(Boolean);
            },
            editorClass() {
                return concat(
                    (this.config.text?.alignment ?? '')
                ).filter(Boolean);
            },
            wrapperClass() {
                return concat(
                    createPaddingClasses(this.config.wrapper?.padding),
                    createMarginClasses(this.config.wrapper?.margin)
                ).filter(Boolean);
            }
        }
    }
</script>
